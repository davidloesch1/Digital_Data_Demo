#!/usr/bin/env node
/**
 * Writes lab_site/js/nexus-env.secrets.js from process.env (Vercel / CI).
 * Set in Vercel: NEXUS_PUBLISHABLE_KEY, optional NEXUS_API_BASE, NEXUS_COLLECT_BASE, NEXUS_DASH_API.
 * Load nexus-env.secrets.js before nexus-env.js in HTML.
 */
const fs = require('fs');
const path = require('path');

const outPath = path.join(__dirname, '..', 'js', 'nexus-env.secrets.js');

function esc(s) {
    return JSON.stringify(s == null ? '' : String(s));
}

const key = process.env.NEXUS_PUBLISHABLE_KEY || '';
const apiBase = process.env.NEXUS_API_BASE || '';
const collectBase = process.env.NEXUS_COLLECT_BASE || '';
const dashApi = process.env.NEXUS_DASH_API || '';

const body = `/* Generated by lab_site/scripts/inject-nexus-env.js — do not hand-edit in CI */
(function () {
    "use strict";
    if (typeof window === "undefined") return;
    var k = ${esc(key)};
    var b = ${esc(apiBase)};
    var c = ${esc(collectBase)};
    var d = ${esc(dashApi)};
    if (k) window.NEXUS_PUBLISHABLE_KEY = k;
    if (b) window.NEXUS_API_BASE = b;
    if (c) window.NEXUS_COLLECT_BASE = c;
    if (d) window.NEXUS_DASH_API = d;
})();
`;

fs.writeFileSync(outPath, body, 'utf8');
const vercelEnv = process.env.VERCEL_ENV || '';
console.log(
    'inject-nexus-env:',
    outPath,
    key ? '(NEXUS_PUBLISHABLE_KEY set)' : '(no NEXUS_PUBLISHABLE_KEY — org paths use defaults until set)'
);
if (!key && vercelEnv) {
    console.log(
        'inject-nexus-env: VERCEL_ENV=' +
            vercelEnv +
            ' — Preview-scoped vars are not applied to Production deployments. If this build is Production, add NEXUS_* under Production or change Settings → Git → Production Branch.'
    );
}
if (!key && (apiBase || collectBase || dashApi)) {
    console.log(
        'inject-nexus-env: NEXUS_API_BASE (or other URL overrides) is set but NEXUS_PUBLISHABLE_KEY is empty — check Vercel: same Preview/Production + branch scope as the key, exact name NEXUS_PUBLISHABLE_KEY, and redeploy.'
    );
}
